    void carregaTabela(){
        String[] colunasTabela = new String[]{"CÃ³digo", "Cliente", "Carro", "Placa", "Retirada", "Entrega Prevista", "Status" };  
        DefaultTableModel modeloTabela = new DefaultTableModel(null,colunasTabela){
            @Override
            public boolean isCellEditable(int row, int column) {
            //all cells false
                return false;
            }
        };
        
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy"); 
        ResultSet resultado = Conexao.buscaAluguel(txtCliente.getText(), txtCarro.getText());
        try {
            while(resultado.next()){
                modeloTabela.addRow(new String[] {
                    resultado.getString("id"),
                    resultado.getString("nome"),
                    resultado.getString("modelo"),
                    resultado.getString("placa"),
                    "", "",
//                    formatter.format(resultado.getDate("data_retirada")),
//                    formatter.format(resultado.getDate("data_entrega")),
                    resultado.getString("descricao")
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaConsultaFabricante.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tbAluguel.setModel(modeloTabela);
    }
    
